package ClassExercises;

public class Exercise05 {

    public static void main(String[] args) {
        int sum = 0; // Variabel för att hålla summan

        // Loopa genom alla tal från 1 till 50 och addera dem till summan
        for (int i = 1; i <= 50; i++) {
            sum += i; // Lägg till varje tal i summan
        }

        // Skriv ut resultatet
        System.out.println("Summan av talen 1 till 50 är: " + sum);
    }
}





